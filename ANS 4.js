/* 
4. Even numbers are divisible by 2 and the remainder is zero. How do you check, if a
number is even or not using JavaScript?
*/

let num=55;

if(num%2==0){
    console.log(`${num} is even.`);
}
else{
    console.log(`${num} is not even.`);
}

// ==========================================OUTPUT==================================
/*
 68 is even.

55 is not even. 
*/